    public void ArithmeticOperations(){
        switch(operation){
            case 1: {
                answer = secondNumber + Double.parseDouble(mainDisplay.getText());
                mainDisplay.setText(Double.toString(answer));
                break;
            }
            case 2: {
                answer = secondNumber - Double.parseDouble(mainDisplay.getText());
                mainDisplay.setText(Double.toString(answer));
                break;
            }
            case 3: {
                answer = secondNumber * Double.parseDouble(mainDisplay.getText());
                mainDisplay.setText(Double.toString(answer));
                break;
            }
            case 4: {
                answer = secondNumber / Double.parseDouble(mainDisplay.getText());
                mainDisplay.setText(Double.toString(answer));
                break;
            }
            case 5: {
                this.currentValue = Double.parseDouble(mainDisplay.getText());
                answer = this.currentValue / 100;
                mainDisplay.setText(Double.toString(answer));
                break;
            }                  
        }
    }

