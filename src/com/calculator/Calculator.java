    private void btn_backspaceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_backspaceActionPerformed
        int mainDisplayLength = mainDisplay.getText().length();
        int mainDisplayLengthMinus1 = mainDisplayLength-1;
        String stored;
        StringBuilder back = new StringBuilder(mainDisplay.getText());

        if(mainDisplayLength > 0){
            back.deleteCharAt(mainDisplayLengthMinus1);
            stored = back.toString(); 
            mainDisplay.setText(stored);
        }
    }//GEN-LAST:event_btn_backspaceActionPerformed

